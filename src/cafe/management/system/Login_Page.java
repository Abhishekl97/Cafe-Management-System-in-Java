    public void validateFields(){
        String email = tf_email.getText();
        String password = tf_password.getText();
        if(email.matches(pattern_email) && !password.equals("")){
            btn_login.setEnabled(true);
        }
        else{
            btn_login.setEnabled(false);
        }
    }
