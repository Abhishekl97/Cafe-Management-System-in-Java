    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        String email = tf_email.getText();
        String password = tf_password.getText();
        Customer customer = null;
        customer = CustomerDataAccessObject.login(email, password);
        if(customer == null){
            JOptionPane.showMessageDialog(null,"<html><b style =\"color:red\">Incorrect Username or Password</b></html>","Message", JOptionPane.ERROR_MESSAGE);
        }
        else{// Navigate to te HomePage
            setVisible(false);
            new HomepageCustomer().setVisible(true);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

