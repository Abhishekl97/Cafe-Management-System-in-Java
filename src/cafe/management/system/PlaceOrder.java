    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String SubCategoryName = model.getValueAt(index, 0).toString();
        SubCategories subcategories = SubCategoriesDataAccessObject.getSubCategoryByName(SubCategoryName);
        txt_subcategory_name.setText(subcategories.getName());
        txt_subcategory_price.setText(subcategories.getPrice());
        jSpinner1.setValue(1);
        txt_subcategory_total.setText(subcategories.getPrice());
        subcategoryPrice = Integer.parseInt(subcategories.getPrice());
        subcategoryTotal = Integer.parseInt(subcategories.getPrice());
        btn_add_to_cart.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked

    private void jSpinner1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinner1StateChanged
        // TODO add your handling code here:
        int quantity = (Integer) jSpinner1.getValue();
        if(quantity <= 1){
            jSpinner1.setValue(1);
            quantity = 1;
        }
        subcategoryTotal = subcategoryPrice * quantity;
        txt_subcategory_total.setText(String.valueOf(subcategoryTotal));
    }//GEN-LAST:event_jSpinner1StateChanged

