    public void validateFields(){
        String customerName = txt_cust_name.getText();
        String customerMobileNumber = txt_cust_mobile_number.getText();
        String customerEmail = txt_cust_email.getText();
        if(!customerName.equals("") && customerMobileNumber.matches(mobile_pattern) && customerMobileNumber.length() == 10 && customerEmail.matches(email_pattern) && finalTotal > 0){
            btn_generate_bill.setEnabled(true);
        }
        else{
            btn_generate_bill.setEnabled(false);
        }
    }

