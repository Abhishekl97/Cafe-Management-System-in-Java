    private void btn_add_to_cartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_to_cartActionPerformed
        // TODO add your handling code here:
        String name = txt_subcategory_name.getText();
        String price = txt_subcategory_price.getText();
        String quantity = String.valueOf(jSpinner1.getValue());
        DefaultTableModel dt = (DefaultTableModel) jTable2.getModel();
        dt.addRow(new Object[]{name, price, quantity, subcategoryTotal});
        finalTotal = finalTotal + subcategoryTotal;
        jLabel14.setText(String.valueOf(finalTotal));
        
        clearSubCategoryFields();
        validateFields();
    }//GEN-LAST:event_btn_add_to_cartActionPerformed

