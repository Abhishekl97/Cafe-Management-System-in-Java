    public void subcategoryNameByCategory(String category){
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        dt.setRowCount(0);
        ArrayList<SubCategories> list = SubCategoriesDataAccessObject.getAllRecordsByCategory(category);
        Iterator<SubCategories> itr = list.iterator();
        while(itr.hasNext()){
            SubCategories subcategories_obj = itr.next();
            dt.addRow(new Object[]{subcategories_obj.getName()});
        }
    }
    
    public void subcategoryNameByCategory(String name, String category){
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        dt.setRowCount(0);
        ArrayList<SubCategories> list = SubCategoriesDataAccessObject.filterSubCategoryByName(name, category);
        Iterator<SubCategories> itr = list.iterator();
        while(itr.hasNext()){
            SubCategories subcategories_obj = itr.next();
            dt.addRow(new Object[]{subcategories_obj.getName()});
        }
    }
    
